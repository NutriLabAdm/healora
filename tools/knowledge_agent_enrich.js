// ── Основной движок ─────────────────────────────────────────────────
function enrichProtocol(protocol) {
  const id = protocol.id;
  const pid = protocol.protocol_id;

  // Заполняем оттуда, где уже есть данные
  let data = { ...protocol };

  // Если протокол уже полностью заполнен (как INFLAMMATORY_SYSTEMIC) — пропускаем
  if (data.research_links && data.research_links.length > 0 &&
      data.usage_methods && data.usage_methods.length > 0 &&
      data.combinations && data.combinations.foods && data.combinations.supplements &&
      data.inhibitors && data.inhibitors.length > 0) {
    return data;
  }

  // 1. Основные протоколы (прямой справочник)
  if (KNOWLEDGE_BASE[id]) {
    const kb = KNOWLEDGE_BASE[id];
    if (!data.research_links && kb.research_links) data.research_links = kb.research_links;
    if (!data.usage_methods && kb.usage_methods) data.usage_methods = kb.usage_methods;
    if (!data.combinations && kb.combinations) data.combinations = kb.combinations;
    if (!data.inhibitors && kb.inhibitors) data.inhibitors = kb.inhibitors;
    return data;
  }

  // 2. Obesity-протоколы: общая база + специфическая
  if (protocol.source === 'obesity') {
    const common = { ...OBESITY_COMMON };
    const specific = OBESITY_SPECIFIC[id] || {};
    
    if (!data.research_links) {
      data.research_links = [
        ...(specific.research_links || []),
        ...common.research_links
      ];
    }
    if (!data.usage_methods) {
      data.usage_methods = [
        ...(specific.usage_methods || []),
        ...common.usage_methods
      ];
    }
    if (!data.combinations) data.combinations = common.combinations;
    if (!data.inhibitors) data.inhibitors = common.inhibitors;
    return data;
  }

  return data;
}

