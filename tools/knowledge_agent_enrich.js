    const hadUsage = p.usage_methods && p.usage_methods.length > 0;
    const hadCombo = p.combinations && p.combinations.foods && p.combinations.supplements;
    const hadInhibitors = p.inhibitors && p.inhibitors.length > 0;
    const hasResearch = result.research_links && result.research_links.length > 0;
    const hasUsage = result.usage_methods && result.usage_methods.length > 0;
    const hasCombo = result.combinations && result.combinations.foods && result.combinations.supplements;
    const hasInhibitors = result.inhibitors && result.inhibitors.length > 0;

    if (hadResearch && hadUsage && hadCombo && hadInhibitors) {
      alreadyFilled++;
    }
    
    if (hasResearch || hasUsage || hasCombo || hasInhibitors) {
      enrichedCount++;
    }

