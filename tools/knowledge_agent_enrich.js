// ── Загрузка данных ─────────────────────────────────────────────────
function loadJSON(filePath) {
  try {
    if (!fs.existsSync(filePath)) {
      console.warn(`  [WARN] File not found: ${filePath}`);
      return null;
    }
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
  } catch (e) {
    console.warn(`  [WARN] Error loading ${filePath}: ${e.message}`);
    return null;
  }
}

